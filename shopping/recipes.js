  // 候補に出す料理名の総合語彙（重複除去）。
  var DISHES = (function () {
    var set = {}, out = [];
    function add(name) {
      var k = normalizeJ(name);
      if (!k || set[k]) return;
      set[k] = true; out.push(name);
    }
    RECIPES.forEach(function (r) { add(r.name); });
    ZUBORA.forEach(function (r) { add(r.name); });
    DISH_GROUPS.forEach(function (g) { g.forEach(add); });
    EXTRA_DISHES.forEach(add);
    return out;
  })();

  // 関連メニュー（同じグループ内のメニュー同士をつなぐ）。
  var RELATED = (function () {
    var m = {};
    DISH_GROUPS.forEach(function (g) {
      g.forEach(function (name) {
        m[name] = m[name] || [];
        g.forEach(function (other) {
          if (other !== name && m[name].indexOf(other) === -1) m[name].push(other);
        });
      });
    });
    return m;
  })();

  // 入力途中の文字列から、候補の料理名を予測して返す（文字列の配列）。
  function predictDishes(query, limit) {
    var qn = normalizeJ(query);
    if (!qn) return [];
    limit = limit || 8;

    var prefix = [], contains = [];
    DISHES.forEach(function (name) {
      var nn = normalizeJ(name);
      if (nn === qn) return;                       // 完全一致はそのまま決定すればよい
      if (nn.indexOf(qn) === 0) prefix.push(name); // 前方一致（最優先）
      else if (nn.indexOf(qn) !== -1) contains.push(name);
    });
    // クエリに近い（短い）ものを上に
    prefix.sort(function (a, b) { return a.length - b.length; });
    contains.sort(function (a, b) { return a.length - b.length; });

    var seen = {}, out = [];
    function push(name) {
      var k = normalizeJ(name);
      if (seen[k]) return;
      seen[k] = true; out.push(name);
    }
    prefix.forEach(push);
    contains.forEach(push);

    // 上位ヒットの「関連メニュー」を後ろに足して幅を出す。
    var base = out.slice();
    for (var i = 0; i < base.length && out.length < limit + 8; i++) {
      var rel = RELATED[base[i]];
      if (rel) rel.forEach(push);
    }
    return out.slice(0, limit);
  }

